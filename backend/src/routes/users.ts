import { Router, Request, Response } from 'express';
import prisma from '../db';
import { apiLimiter } from '../middlewares/security';
import { authenticateJWT, authorizeRoles } from '../middlewares/auth';

const router = Router();

// Get all users (for managers and admins to assign tasks)
router.get('/', authenticateJWT, authorizeRoles('manager', 'admin'), async (req: Request, res: Response) => {
  try {
    const users = await prisma.user.findMany({
      select: {
        id: true,
        name: true,
        email: true,
        role: true,
      },
      orderBy: { name: 'asc' },
    });

    res.json({
      success: true,
      data: users,
    });
  } catch (error) {
    console.error('Error fetching users:', error);
    res.status(500).json({
      success: false,
      error: 'Failed to fetch users',
    });
  }
});

// Get user by ID (for managers and admins)
router.get('/:id', authenticateJWT, authorizeRoles('manager', 'admin'), async (req: Request, res: Response) => {
  try {
    const { id } = req.params;
    const user = await prisma.user.findUnique({
      where: { id: parseInt(id) },
      select: {
        id: true,
        name: true,
        email: true,
        role: true,
      },
    });

    if (!user) {
      return res.status(404).json({
        success: false,
        error: 'User not found',
      });
    }

    res.json({
      success: true,
      data: user,
    });
  } catch (error) {
    console.error('Error fetching user:', error);
    res.status(500).json({
      success: false,
      error: 'Failed to fetch user',
    });
  }
});

export default router; 